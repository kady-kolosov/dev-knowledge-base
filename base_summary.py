'''
Если множество X содержит n элементов, то оно имеет 2**n подмножеств,
включая пустое множество. Например, множество X = {a, b, c} содержит 3 элемента
и имеет 8 подмножеств:
{
    ∅
}
1. {∅}
2. {a}
3. {b}
4. {c}
5. {a, b}
6. {a, c}
7. {b, c}
8. {a,b,c}
'''


'''
1.  все элементы множества уникальны
2.  множества неупорядочены
3.  элементы множества должны относиться к неизменяемым типам данных
4.  хранящиеся в множестве элементы могут иметь разные типы данных
5.  Чтобы создать множество, нужно перечислить его элементы через запятую в
    фигурных скобках numbers = {2, 4, 6, 8, 10}. Создать пустое множество можно
    с помощью встроенной функции, которая называется set(), создать пустое
    множество с помощью пустых фигурных скобок нельзя, c помощью
    пустых фигурных скобок создаются словари... так сложилось исторически.
    Дело в том, что словари появились в Python раньше, чем множества
8.  индексация и срезы недоступны для множеств
9.  Операция конкатенации "+" и умножения на число "*" недоступны для множеств
'''
# ────────────────────────────────────────────────────────────────────

myset1 = {1, 2, 3, 4, 5}
myset2 = {3, 4, 6, 7, 8}

# Объединение (возвращает новое множество)
res = myset1.union(myset2)
res = myset1 | myset2
print(res)  # {1, 2, 3, 4, 5, 6, 7, 8}

# Пересечение (возвращает новое множество)
res = myset1.intersection(myset2)
res = myset1 & myset2
print(res)  # {3, 4}

# Разность (возвращает новое множество). Удаляет пересекающиеся элементы
# в первом множестве
res = myset1.difference(myset2)
res = myset1 - myset2
print(res)  # {1, 2, 5}

# Симметрическая разность (возвращает новое множество). Объединяет, удаляя
# пересекающиеся элементы
res = myset1.symmetric_difference(myset2)
res = myset1 ^ myset2
print(res)  # {1, 2, 5, 6, 7, 8}

# ────────────────────────────────────────────────────────────────────

myset1 = {1, 2, 3, 4, 5}
myset2 = {3, 4, 6, 7, 8}

# Объединение (изменяет исходное множество)
myset1.update(myset2)
myset1 |= myset2
print(myset1)

# Пересечение (изменяет исходное множество)
myset1.intersection_update(myset2)
myset1 &= myset2
print(myset1)

# Разность (изменяет исходное множество). Удаляет пересекающиеся элементы
# в первом множестве
myset1.difference_update(myset2)
myset1 -= myset2
print(myset1)

# Симметрическая разность (изменяет исходное множество). Объединяет, удаляя
# пересекающиеся элементы
myset1.symmetric_difference_update(myset2)
myset1 ^= myset2
print(myset1)

