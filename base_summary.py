# !Заполнение выше и ниже главной диагонали и побочной диагонали
n = int(input('Введите размерность матрицы: '))
matrix = [[0] * n for _ in range(n)]

for i in range(n):
    for j in range(n):
        if i == j:  # !главная диагональ
            matrix[i][j] = 1
        elif i < j:  # выше главной диагонали
            matrix[i][j] = 2
        elif i > j:  # ниже главной диагонали
            matrix[i][j] = 3
        elif i + j == n - 1:  # !побочная диагональ
            matrix[i][j] = 11
        elif i + j < n - 1:  # выше побочной диагонали
            matrix[i][j] = 33
        elif i + j > n - 1:  # ниже побочной диагонали
            matrix[i][j] = 22

for i in range(n):
    for j in range(n):
        print(f'{matrix[i][j]:<2}', end=' ')
    print()

