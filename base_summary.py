# !Главная и побочная диагонали квадратной матрицы
# создаем квадратную матрицу размером n×n
n = int(input('Введите размерность матрицы: '))
matrix = [[0]*n for _ in range(n)]

# заполняем главную диагональ единицами, а побочную двойками
for i in range(n):
    matrix[i][i] = 1  # главная (сверху слева к низу справа)
    matrix[i][n-i-1] = 2  # побочная (свеху справа к низу слева)

for r in range(n):  # выводим матрицу
    for c in range(n):
        print(matrix[r][c], end=' ')
    print()

